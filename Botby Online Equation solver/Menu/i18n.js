  /**
   * Translate a key, optionally substituting {placeholder} variables.
   * Falls back: current lang → sv → key itself.
   */
  window.t = function (key, vars) {
    var lang = getStoredLang();
    var dict = translations[lang] || translations["sv"];
    var str = dict[key];
    if (str === undefined) str = translations["sv"][key];
    if (str === undefined) str = key;
    if (vars) {
      Object.keys(vars).forEach(function (k) {
        str = str.replace(new RegExp("\\{" + k + "\\}", "g"), String(vars[k]));
      });
    }
    return str;
  };

  /**
   * Persist a language choice and refresh all [data-i18n] elements on the page.
   * Call with no argument to apply the saved / default language on page load.
   */
  window.applyLanguage = function (lang) {
    if (lang) setStoredLang(lang);
    var current = getStoredLang();

    document.documentElement.lang = current;

    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      el.textContent = window.t(el.dataset.i18n);
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
      el.placeholder = window.t(el.dataset.i18nPlaceholder);
    });

    var sel = document.getElementById("lang-select");
    if (sel) sel.value = current;

    document.dispatchEvent(new CustomEvent("langchange", { detail: { lang: current } }));
  };
